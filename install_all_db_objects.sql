            p_export_id => p_export_id,
            p_template_type => 'LIST',
            p_template_options => v_value
          );
        END IF;
        
        v_am_tree_parameters.extend;
        v_am_tree_parameters(v_am_tree_parameters.count).call_id := v_call_id; 

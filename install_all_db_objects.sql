    v_scope        varchar2(500);
  begin
    for i in (
      select level, regexp_substr(p_scope,'[^/]+',1,level) as tree_items
      from dual
      connect by LEVEL <= REGEXP_COUNT(p_scope ,'[/]') + 1
    ) 
    loop
      begin
         v_scope := v_scope||'/'||i.tree_items;
         select call_id
         into v_call_id
         from am_scope
         where scope = v_scope;

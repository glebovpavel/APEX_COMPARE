          'BODY_'||a,    
          p_template_apex_id,
          v_entry_point,
          v_distance + a,
          1
        );
      end loop;  
    end if;

    for i in (
      select placeholder, 
             row_number() over(order by entry_point nulls last, distance nulls last ) as rn 
      from am_templates_display_points
      where export_id = p_export_id
        and template_apex_id = p_template_apex_id) 
